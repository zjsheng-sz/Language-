
cmd + t	模糊搜索工作目录下的文件
cmt + b	搜索已经打开的文件
ctrl + 0	焦点移动到文件目录
cmd + \	隐藏左侧目录树
ctrl + [	目录中展开结点
ctrl + ]	目录中缩起结点
alt + ctl + [ 目录中展开所有
alt + ctl + ] 目录中收起所有
delete / a / m	对目录选中的结点进行 删除 / 添加 / 移动
cmd + f	搜索 当前文件
cmd + shift + f	搜索 目录
# cmd + shift + r	搜索 tag 前提是安装ctags (brew install ctags)且生成tags (ctags -R src/)
alt + cmd + [	收起 所在代码块
alt + cmd + ]	展开 所在代码块
alt + cmd + shift + [	收起 当前所有代码块
alt + cmd + shift + ]	展开 当前所有代码块
cmd + ,	settings
# cmd + k ... 方向键	切分面板
# cmd + k ... cmd + 方向	切换面板
cmd + w	关闭当前工作面板
cmd + shift + w 关闭当前窗口
alt + cmd + 左右	切换tab文件选项卡
ctl + shift + c  复制路径
